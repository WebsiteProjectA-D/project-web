package model;

public class Specialite {
	
	private int id;
	private String nom;
	
	public Specialite(String nom) {
		this.nom = nom;
	}

	public Specialite() {
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}
	
	
	
	

}
